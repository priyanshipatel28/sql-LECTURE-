-- Data query langauge:-

-- Q= I JUAT WANT TO DIAPLAY EMPLOYEE_ID, FIRST_NAME, LAST_NAME.
SELECT EMPLOYEE_ID,FIRST_NAME,LAST_NAME FROM EMPLOYEES;

-- Q= GET THE DATA WHOSE SALARY > 10,000.
SELECT * FROM EMPLOYEES WHERE SALARY > 10000;

-- Q= GET THE DATA WHO HAS COMISSION_PST.
SELECT * FROM EMPLOYEES WHERE COMPISSION_PST IS NOT NULL;

-- Q= GET THE DATA WHOSE DEPARTMENT_ID IS NOT NUL.
SELECT * FROM EMPLOYEES WHERE DEPARTMENT_ID IS NOT NULL;

-- Q= GET THE DATA WHOSE MANAGER_ID IS 149.
SELECT * FROM EMPLOYEES WHERE MANAGER_ID = 149;
-- JUST PRACTICE
SELECT * FROM EMPLOYEES WHERE MANAGER_ID = 149 AND DEPARTMENT_ID IS NULL;

-- TO ASCENDING AND DECENDING THE DATA
-- HERE WE WILL USE ORDER BY CLAUSE.

-- Q= GET THE DATA WITH ORDER BY CLAUSE
SELECT * FROM EMPLOYEES ORDER BY SALARY;  -- it will order it in ascending order and it is a default part.alter
SELECT * FROM EMPLOYEES ORDER BY SALARY ASC;

-- Q= GET THE DATA IN DECEDNING ORDER 
SELECT * FROM EMPLOYEES ORDER BY SALARY DESC;

-- ORDER BY HAS THREE WAYS TO DISPLAY:-
-- FIRST WAY
SELECT * FROM EMPLOYEES ORDER BY 6;
-- SECOND WAY
SELECT * FROM EMPLOYEES ORDER BY SALARY;
-- THIRD WAY(IT WILL JUST CHANGE THE NAME OF SALARY AS SAL AS TEMPORARY BASE)
SELECT FIRST_NAME,SALARY AS SAL FROM EMPLOYEES ORDER BY SAL;

-- To temporarily change the name of a column in a query, you use the AS keyword.
-- In SQL, the AS keyword allows you to rename a column or a table temporarily within the scope of the query. 
-- This new name is called an alias.
-- EXAMPLE
SELECT FIRST_NAME AS employee_name FROM employees;

-- TO MERGE/JOIN THE COLUMN (CONCAT WILL USE)
-- INBUILT FUNCTION --> CONCAT()
-- Q= WE WANT TO DISPLAY EMPLOYEE_ID, FULL NAME, SALARY:-

SELECT EMPLOYEE_ID,CONCAT(FIRST_NAME,CONCAT(" ",LAST_NAME)) AS FULL_NAME, SALARY FROM EMPLOYEES; 

